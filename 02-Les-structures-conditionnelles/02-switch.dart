void main(List<String> args) {
  var jour = 5;

  switch (jour) {
    case 1:
      print('Lundi');
      break;
    case 2:
      print('Mardi');
      break;
    case 3:
      print('Mercredi');
      break;
    case 4:
      print('Jeudi');
      break;
    case 5:
      print('Vendredi');
      break;
    case 6:
      print('Samedi');
      break;
    case 7:
      print('Dimanche');
      break;
    default:
      print('Jour Inconnu');
      break;
  }
}
